    private void button_impOraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_impOraActionPerformed
        // TODO add your handling code here:
        String t = tP_ora.getText();
        if (t != ""){
            if (t.length() == 4){
                int metà = t.length() / 2;
                sv.set_ora(Integer.parseInt(t.substring(0, metà)), Integer.parseInt(t.substring(metà)));
                lbl_ora.setText(sv.get_orario());
                lbl_schermo.setText(sv.get_orario());
                tP_ora.setText("");
            }
        }
        else {
            sv.set_ora(0, 0);
            lbl_ora.setText("00:00");
            lbl_schermo.setText("00:00");
        }
    }//GEN-LAST:event_button_impOraActionPerformed

