    private void button_randomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_randomActionPerformed

        Random r = new Random();
        this.lbl_volume.setText("" + r.nextInt(11));
        
        sv.set_oraSveglia(r.nextInt(25), r.nextInt(61));
        this.lbl_svegl.setText(sv.get_oraS() + ":" + sv.get_minS()); 
        
        sv.set_ora(r.nextInt(25-sv.get_oraS()), r.nextInt(61-sv.get_minS()));
        this.lbl_ora.setText(sv.get_ora() + ":" + sv.get_min());
        this.lbl_schermo.setText(sv.get_orario());
        
        this.lbl_data.setText(r.nextInt(31) + "/" + r.nextInt(13) +"/"+r.nextInt(1000, 3000));
        this.tB_day.setText("");
        this.tB_month.setText("");
        this.tP_ora.setText("");
        this.tP_svegl.setText("");
        this.tB_year.setText("");
    }//GEN-LAST:event_button_randomActionPerformed

    private void button_avvioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_avvioActionPerformed

        Timer t = new Timer(1000, e-> {
            sv.aumentoMin();
            this.lbl_schermo.setText(sv.get_orario());
            if (sv.get_orario().equals(sv.get_orarioS())){
                ((Timer) e.getSource()).stop();  
                this.buttom_disattiva.setVisible(true);
                this.buttom_snooze.setVisible(true);
                lbl_staz.setText("" + cB_staz.getSelectedItem());
            }
            lbl_data.setText(data.get_data());
        });
        t.start();

//this.lblblbl.setText("" + sv.get_timerMS());
        
    }//GEN-LAST:event_button_avvioActionPerformed

    private void buttom_snoozeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttom_snoozeActionPerformed
        // TODO add your handling code here:
        this.buttom_disattiva.setVisible(false);
        this.buttom_snooze.setVisible(false);
        sv.set_oraSveglia(sv.get_oraS(), sv.get_minS() + 5);
        Timer t = new Timer(1000, e->{
            sv.aumentoMin();
            this.lbl_schermo.setText(sv.get_orario()); 
            if (sv.get_orario().equals(sv.get_orarioS())){
                this.buttom_disattiva.setVisible(true);
                this.buttom_snooze.setVisible(true);
                ((Timer) e.getSource()).stop();  
            }
            
        });
        t.start();
    }//GEN-LAST:event_buttom_snoozeActionPerformed

